package Loops.whileLoops;

public class WhileLoop_3 {

    public static void execute() {
        int n = 1;

        while (n <= 10) {
            System.out.println(n+"  "+"While loop executing");
            //System.out.println(n);
            n++;        }
    }

    public static void main(String[] args) {
        WhileLoop_3.execute();
    }
}
